/**
 * 系统管理/用户管理
 *
 * 删除用户
 *
 */
export async function deleteSystemUserById(
  id?: string | number,
): Promise<null> {
  const res = await useRequest({
    url: `/system/user/${id}`,
    method: 'delete',
  })
  return res.data.data
}
